#ifndef GENDER_H
#define GENDER_H

/**
 * @brief The Gender enumeration represents different genders.
 *
 * This enumeration defines two gender options: Female and Male. It is used to represent
 * gender-related information in a concise and readable manner throughout the codebase.
 *
 * Usage example:
 * \code
 *     Gender userGender = Female;
 *
 * \endcode
 */
enum Gender {
    Female, ///< Represents the female gender.
    Male    ///< Represents the male gender.
};

#endif // GENDER_H
